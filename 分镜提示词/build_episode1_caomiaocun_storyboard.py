def style_sheet(ws):
    ws.freeze_panes = "A5"
    ws.sheet_view.showGridLines = False
    widths = [8, 20, 14, 24, 62, 10, 30, 34, 34, 68]
    for i, width in enumerate(widths, start=1):
        ws.column_dimensions[get_column_letter(i)].width = width

    thin = Side(style="thin", color="D9D9D9")
    border = Border(left=thin, right=thin, top=thin, bottom=thin)
    title_fill = PatternFill("solid", fgColor="7F4E1E")
    section_fill = PatternFill("solid", fgColor="FCE4D6")
    header_fill = PatternFill("solid", fgColor="C55A11")
    note_fill = PatternFill("solid", fgColor="FFF2CC")

    for row in ws.iter_rows():
        for cell in row:
            cell.alignment = Alignment(vertical="top", wrap_text=True)
            cell.border = border
            cell.font = Font(name="PingFang SC", size=11)

    for row_idx in [1, 2]:
        ws.merge_cells(start_row=row_idx, start_column=1, end_row=row_idx, end_column=10)
        cell = ws.cell(row_idx, 1)
        cell.fill = title_fill if row_idx == 1 else PatternFill("solid", fgColor="FDF2EA")
        cell.font = Font(name="PingFang SC", size=16 if row_idx == 1 else 11, bold=True, color="FFFFFF" if row_idx == 1 else "1F1F1F")
        cell.alignment = Alignment(horizontal="center", vertical="center", wrap_text=True)
        ws.row_dimensions[row_idx].height = 30 if row_idx == 1 else 44

    for row_idx in range(1, ws.max_row + 1):
        first = ws.cell(row_idx, 1).value
        if isinstance(first, str) and "幕 ·" in first:
            ws.merge_cells(start_row=row_idx, start_column=1, end_row=row_idx, end_column=10)
            cell = ws.cell(row_idx, 1)
            cell.fill = section_fill
            cell.font = Font(name="PingFang SC", size=12, bold=True, color="7F4E1E")
            cell.alignment = Alignment(horizontal="left", vertical="center", wrap_text=True)
            ws.row_dimensions[row_idx].height = 26
        elif first == "镜号":
            for col in range(1, 11):
                cell = ws.cell(row_idx, col)
                cell.fill = header_fill
                cell.font = Font(name="PingFang SC", size=11, bold=True, color="FFFFFF")
                cell.alignment = Alignment(horizontal="center", vertical="center", wrap_text=True)
            ws.row_dimensions[row_idx].height = 24
        elif isinstance(first, str) and first.startswith("【"):
            ws.merge_cells(start_row=row_idx, start_column=1, end_row=row_idx, end_column=10)
            cell = ws.cell(row_idx, 1)
            cell.fill = note_fill
            cell.font = Font(name="PingFang SC", size=11, bold=True, color="7F6000")
            ws.row_dimensions[row_idx].height = 70
        elif first:
            ws.row_dimensions[row_idx].height = 58


